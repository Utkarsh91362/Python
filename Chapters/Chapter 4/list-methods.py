l1=[1,34,51,21,4,62]

print(l1)


#adding value at the end of the list
l1.append(2)
print("append")
print(l1)

#sorting in ascending order
l1.sort()
print("sorting")
print(l1)

# reverse
l1.reverse()
print("reverse")
print(l1)


#adding value at a specific index
l1.insert(4,689)
print("adding")
print(l1)


#deleting element at a particular index
l1.pop(3)
print("pop")
print(l1)

#deleting element using value not index
l1.remove(34)
print("remove a value")
print(l1)

